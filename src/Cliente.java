public class Cliente {
    private String numeroCliente;
    private String sobrenome;
    private String cpf;
    private int conta;

    public Cliente(String numeroCliente, String sobrenome, String cpf, int conta) {
        this.numeroCliente = numeroCliente;
        this.sobrenome = sobrenome;
        this.cpf = cpf;
        this.conta = conta;
    }
}
